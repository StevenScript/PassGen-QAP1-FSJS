// Get the command-line arguments (starting from 2)
const args = process.argv.slice(2);

// Default values
// Set to true so default password is most complex
let length = 8; // Default length of password
let includeNumbers = true;
let includeUppercase = true;
let includeSymbols = true;

// Parse the arguments
for (let i = 0; i < args.length; i++) {
  if (args[i] === "--setlength" && args[i + 1]) {
    length = parseInt(args[i + 1], 10);
    i++; // Skip the next argument as it's the value for --length
  }
  if (args[i] === "--nonumbers") {
    includeNumbers = false;
  }
  if (args[i] === "--nouppercase") {
    includeUppercase = false;
  }
  if (args[i] === "--nosymbols") {
    includeSymbols = false;
  }
}

// List of Allowed Characters
const UPPER_LETTERS = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
const LOWER_LETTERS = "abcdefghijklmnopqrstuvwxyz";
const DIGITS = "1234567890";
const SPECIAL_CHARACTERS = "!#%^&*";

// Start with the default character set (lowercase letters)
let characterSet = LOWER_LETTERS;

// Build the character set based on the options provided
if (includeUppercase) {
  characterSet += UPPER_LETTERS; // Add uppercase letters unless told otherwise
}
if (includeNumbers) {
  characterSet += DIGITS; // Add numbers unless told otherwise
}
if (includeSymbols) {
  characterSet += SPECIAL_CHARACTERS; // Add special characters unless told otherwise
}

// Function to generate a password
const generatePassword = (len) => {
  let password = "";
  for (let i = 0; i < len; i++) {
    const randomIndex = Math.floor(Math.random() * characterSet.length);
    password += characterSet[randomIndex];
  }
  return password;
};

// Generate password using the length from command line arguments
const generatedPassword = generatePassword(length);
console.log(`Generated Password: ${generatedPassword}`);
